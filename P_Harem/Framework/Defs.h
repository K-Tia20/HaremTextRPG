#include "Framework.h"
#include "../Creature/Creature.h"

using namespace std;

using OnLevelTextNumDelegate = std::function<void(std::string, int)>;
// ๏ฟฝุฝ๏ฟฝฦฎ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝฺธ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦฎ;

using OnLevelTextNumNumDelegate = std::function<void(std::string, int, int)>;
// ๏ฟฝุฝ๏ฟฝฦฎ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ 2๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦฎ;

class C_LevelSystem
{
public:
	OnLevelTextNumNumDelegate OnGainAffinity;
	OnLevelTextNumDelegate OnLevelUp;

	int RequiredAffinity[9] = { 100, 100, 150, 150, 200, 250, 300, 300, 300 };


	int GetRequiredAffinity(std::shared_ptr<C_Creature> Creature) const
	{
		if (Creature->GetLevel() >= 10)
			return 0;
		return RequiredAffinity[Creature->GetLevel() - 1];
	} 
	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝสฟ๏ฟฝ ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝศฏ๏ฟฝฯด๏ฟฝ ๏ฟฝิผ๏ฟฝ;
	
	void GainAffinity(std::shared_ptr<C_Creature> Creature, int Affinity) 
	{
		Creature->SetAffinity(Creature->GetAffinity() + Affinity);
		CheckLevelUp(Creature);
		if (OnGainAffinity)
		{
			OnGainAffinity(Creature->GetName(), Creature->GetAffinity(), GetRequiredAffinity(Creature));
			// ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤก)ศน๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝฬธ๏ฟฝ, ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ, ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝสฟ๏ฟฝ ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ;
		}
	} 
	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤก)๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลฐ๏ฟฝ๏ฟฝ ๏ฟฝิผ๏ฟฝ. ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ รผลฉ;


	void CheckLevelUp(std::shared_ptr<C_Creature> Creature)
	{
		if (Creature->GetLevel() < 10 && Creature->GetAffinity() >= GetRequiredAffinity(Creature))
		{
			LevelUp(Creature);
			Creature->SetAffinity(Creature->GetAffinity() - GetRequiredAffinity(Creature));
		}
	}
	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ ๏ฟฝิผ๏ฟฝ. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯธ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯฐ๏ฟฝ, ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯฟ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ;


	void LevelUp(std::shared_ptr<C_Creature> Creature)
	{
		Creature->SetLevel(Creature->GetLevel() + 1);
		Creature->SetMaxHp(Creature->GetMaxHp() + Creature->GetLevel() * 20);
		Creature->SetAttack(Creature->GetAttack() + Creature->GetLevel() * 5);
		Creature->SetCurrentHp(Creature->GetMaxHp());
		if (OnLevelUp)
		{
			OnLevelUp(Creature->GetName(), Creature->GetLevel());
			// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝฬธ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ;
		}
	}
	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลฒ ๏ฟฝ๏ฟฝ ๏ฟฝฬธ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ


};
